def get_template(name):
    """获取模板"""
    return TEMPLATES.get(name, TEMPLATES["标准版"])

def print_template(template, name=""):
    """打印模板"""
    print(f"\n📝 案例模板：{name}\n")
    print("=" * 70)
    print(template)
    print("=" * 70)
    print("\n💡 使用说明:")
    print("  将 {xxx} 占位符替换为实际内容")
    print("  可根据需要增删章节\n")

if __name__ == '__main__':
    parser = argparse.ArgumentParser(description='案例模板库')
    parser.add_argument('-t', '--type', default='标准版',
                       choices=['标准版', '技术版', '故事版', '一图流'],
                       help='模板类型')
    parser.add_argument('-o', '--output', help='输出文件')

    args = parser.parse_args()

    template = get_template(args.type)
    print_template(template, args.type)

    if args.output:
        with open(args.output, 'w', encoding='utf-8') as f:
            f.write(template)
        print(f"✅ 已保存到：{args.output}")
